    private void btn_enregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enregistrerActionPerformed
        Type_ServiceDAO edao = new Type_ServiceDAO();
        ServiceDAO sdao = new ServiceDAO();

        //String Designation_tps = txt_designationtps0.getText();
        String Designation_tps = txt_designationtps.getSelectedItem().toString();
        //String IDS = txt_tps.getSelectedItem().toString();
        String LibelleS = txt_tps.getSelectedItem().toString();

        if (Designation_tps.equals("") || LibelleS.equals("")) {
            JOptionPane.showMessageDialog(this, "Veillez renseigner tous les champs!");
        } else {
            int IDS = sdao.getIdService(LibelleS);
            Type_Service tps = new Type_Service(Designation_tps, sdao.getIdService(LibelleS));
            
            edao.inserer(tps);
            vider();
            liste_tps();
        }
    }//GEN-LAST:event_btn_enregistrerActionPerformed

    private void btn_supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_supprimerActionPerformed
        int index = tb_tps.getSelectedRow();

        if(index==-1) {
            JOptionPane.showMessageDialog(this, "Veillez selectionner une ligne!");
        } else {
            int id = (int) tb_tps.getValueAt(index, 0);
            Type_ServiceDAO edao = new Type_ServiceDAO();
            edao.supprimer(id);
            vider();
            liste_tps();
        }
    }//GEN-LAST:event_btn_supprimerActionPerformed


    private void txt_rechercherKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_rechercherKeyReleased
        DefaultTableModel model = (DefaultTableModel) tb_tps.getModel();
        TableRowSorter sorter = new TableRowSorter(model);
        tb_tps.setRowSorter(sorter);
        String rech = txt_rechercher.getText();
        sorter.setRowFilter(RowFilter.regexFilter(rech));
    }//GEN-LAST:event_txt_rechercherKeyReleased

    
    
    private void combo_tps() {
        ServiceDAO sdao = new ServiceDAO();
        List<Service> service = sdao.liste();
        txt_tps.addItem("");
        for(int i = 0; i < service.size(); i++) {
            //txt_tps.addItem(service.get(i).getIDS()+"");
            txt_tps.addItem(service.get(i).getLibelleS());
            sdao.rechercher(service.get(i).getIDS()).getLibelleS();
       }
    }
    
 
    private void liste_tps() {
        Type_ServiceDAO edao = new Type_ServiceDAO();
        ServiceDAO sdao = new ServiceDAO();
        
        List<Type_Service> type_service = edao.liste();
        
        for(int i = 0; i < type_service.size(); i++) {
            ((DefaultTableModel) tb_tps.getModel()).addRow(new Object[] {
            type_service.get(i).getID_tps(),
            type_service.get(i).getDesignation_tps(),
            //type_service.get(i).getIDS(),
            sdao.rechercher(type_service.get(i).getIDS()).getLibelleS(),
            
        });
       }
    }

   public void vider() {
        //txt_designationtps0.setText("");
        txt_designationtps.setSelectedItem("");
        txt_tps.setSelectedItem("");
        
        DefaultTableModel model = (DefaultTableModel) tb_tps.getModel();
        int n = model.getRowCount();
        for(int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
        }
   }
    
    
    


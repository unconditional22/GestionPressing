    private void tb_tpsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_tpsMouseClicked
        int index = tb_tps.getSelectedRow();

        if(index!=-1) {
            int id = (int) tb_tps.getValueAt(index, 0);
            Type_ServiceDAO edao = new Type_ServiceDAO();
            Type_Service tpsl = edao.rechercher(id);

            //txt_designationtps0.setText(tpsl.getDesignation_tps());
            txt_designationtps.setSelectedItem(tpsl.getDesignation_tps());
            txt_tps.setSelectedItem(tpsl.getIDS());
        }
    }//GEN-LAST:event_tb_tpsMouseClicked


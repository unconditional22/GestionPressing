    private void tb_retraitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_retraitMouseClicked
        int index = tb_retrait.getSelectedRow();
        
        if(index!=-1) {
            int id = (int) tb_retrait.getValueAt(index, 0);
            RetirerDAO edao = new RetirerDAO();
            Retirer retirel = edao.rechercher(id);
            
            txt_dateRetrait.setDate(retirel.getDate_retrait());
            txt_QtRetrait.setText(retirel.getQt_retrait()+"");
            txt_article.setSelectedItem(retirel.getIDA()+"");
            txt_client.setSelectedItem(retirel.getID_Client()+"");
            
        } 
    }//GEN-LAST:event_tb_retraitMouseClicked


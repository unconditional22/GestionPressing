    private void txt_rechercherKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_rechercherKeyReleased
        DefaultTableModel model = (DefaultTableModel) tb_retrait.getModel();
        TableRowSorter sorter = new TableRowSorter(model);
        tb_retrait.setRowSorter(sorter);
        String rech = txt_rechercher.getText();
        sorter.setRowFilter(RowFilter.regexFilter(rech));
    }//GEN-LAST:event_txt_rechercherKeyReleased

    private void combo_article() {
        ArticleDAO adao = new ArticleDAO();
        List<Article> article = adao.liste();
        txt_article.addItem("");
        for(int i = 0; i < article.size(); i++) {
            //txt_article.addItem(article.get(i).getIDA()+"");
            txt_article.addItem(article.get(i).getLibelleA());
            adao.rechercher(article.get(i).getIDA()).getLibelleA();
       }
    }

private void combo_client() {
        ClientDAO cdao = new ClientDAO();
        List<Client> client = cdao.liste();
        txt_client.addItem("");
        for(int i = 0; i < client.size(); i++) {
            //txt_client.addItem(client.get(i).getID_Client()+"");
            txt_client.addItem(client.get(i).getPrenom());
            cdao.rechercher(client.get(i).getID_Client()).getPrenom();
       }
    }
    
 
    private void liste_retirer() {
        RetirerDAO edao = new RetirerDAO();
        ArticleDAO adao = new ArticleDAO();
        ClientDAO cdao = new ClientDAO();
        
        List<Retirer> retirer = edao.liste();
        
        for(int i = 0; i < retirer.size(); i++) {
            ((DefaultTableModel) tb_retrait.getModel()).addRow(new Object[] {
            retirer.get(i).getId_retrait(),
            retirer.get(i).getDate_retrait(),
            retirer.get(i).getQt_retrait(),
            
            //retirer.get(i).getIDA(),
            adao.rechercher(retirer.get(i).getIDA()).getLibelleA(),
            
            //retirer.get(i).getID_Client(),
            cdao.rechercher(retirer.get(i).getID_Client()).getPrenom(),
                    
            
        });
       }
    }

   public void vider() {
       txt_dateRetrait.setDate(null); 
       txt_QtRetrait.setText("");
       txt_article.setSelectedItem("");
       txt_client.setSelectedItem("");
        
        DefaultTableModel model = (DefaultTableModel) tb_retrait.getModel();
        int n = model.getRowCount();
        for(int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
        }
   }
   
   

    private void tb_tpaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_tpaMouseClicked
        int index = tb_tpa.getSelectedRow();

        if(index!=-1) {
            int id = (int) tb_tpa.getValueAt(index, 0);
            Type_ArticleDAO edao = new Type_ArticleDAO();
            Type_Article tpal = edao.rechercher(id);

            //txt_designationtpa0.setText(tpal.getDesignation_tpa());
            txt_designationtpa.setSelectedItem(tpal.getDesignation_tpa());
            txt_tpa.setSelectedItem(tpal.getIDA());
        }
    }//GEN-LAST:event_tb_tpaMouseClicked

    private void btn_supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_supprimerActionPerformed
        int index = tb_tpa.getSelectedRow();

        if(index==-1) {
            JOptionPane.showMessageDialog(this, "Veillez selectionner une ligne!");
        } else {
            int id = (int) tb_tpa.getValueAt(index, 0);
            Type_ArticleDAO edao = new Type_ArticleDAO();
            edao.supprimer(id);
            vider();
            liste_tpa();
        }
    }//GEN-LAST:event_btn_supprimerActionPerformed

    private void btn_modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modifierActionPerformed
        Type_ArticleDAO edao = new Type_ArticleDAO();
        ArticleDAO adao = new ArticleDAO();

        //String Designation_tpa = txt_designationtpa0.getText();
        String Designation_tpa = txt_designationtpa.getSelectedItem().toString();
        //String IDA = txt_tpa.getSelectedItem().toString();
        String LibelleA = txt_tpa.getSelectedItem().toString();


        if (Designation_tpa.equals("") || LibelleA.equals("")) {
            JOptionPane.showMessageDialog(this, "Veillez renseigner tous les champs!");
        } else {
            int index = tb_tpa.getSelectedRow();
            int id = (int) tb_tpa.getValueAt(index, 0);
            Type_Article tpa = new Type_Article(Designation_tpa, adao.getIdArticle(LibelleA));
            int IDA = adao.getIdArticle(LibelleA);
            edao.modifier(tpa, id);
            vider();
            liste_tpa();
        }
    }//GEN-LAST:event_btn_modifierActionPerformed


    private void txt_rechercherKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_rechercherKeyReleased
        DefaultTableModel model = (DefaultTableModel) tb_tpa.getModel();
        TableRowSorter sorter = new TableRowSorter(model);
        tb_tpa.setRowSorter(sorter);
        String rech = txt_rechercher.getText();
        sorter.setRowFilter(RowFilter.regexFilter(rech));
    }//GEN-LAST:event_txt_rechercherKeyReleased

    
    private void combo_tpa() {
        ArticleDAO adao = new ArticleDAO();
        List<Article> article = adao.liste();
        txt_tpa.addItem("");
        for(int i = 0; i < article.size(); i++) {
            //txt_tpa.addItem(article.get(i).getIDA()+"");
            txt_tpa.addItem(article.get(i).getLibelleA());
            adao.rechercher(article.get(i).getIDA()).getLibelleA();
            
       }
    }
    
 
    private void liste_tpa() {
        Type_ArticleDAO edao = new Type_ArticleDAO();
        ArticleDAO adao = new ArticleDAO();
        
        List<Type_Article> type_article = edao.liste();
        
        for(int i = 0; i < type_article.size(); i++) {
            ((DefaultTableModel) tb_tpa.getModel()).addRow(new Object[] {
            type_article.get(i).getID_tpa(),
            type_article.get(i).getDesignation_tpa(),
            //type_article.get(i).getIDA(),
            adao.rechercher(type_article.get(i).getIDA()).getLibelleA(),
            
        });
       }
    }

   public void vider() {
        //txt_designationtpa0.setText("");
        txt_designationtpa.setSelectedItem("");
        txt_tpa.setSelectedItem("");
        
        DefaultTableModel model = (DefaultTableModel) tb_tpa.getModel();
        int n = model.getRowCount();
        for(int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
        }
   }
    
    


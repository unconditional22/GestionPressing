    private void btn_enregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enregistrerActionPerformed
        ServiceDAO edao = new ServiceDAO();

        String LibelleS = txt_libelles.getSelectedItem().toString();
        String NumS = txt_nums.getText();
        String PrixUS = txt_prixus.getText();

        if (LibelleS.equals("") || NumS.equals("") || PrixUS.equals("")) {
            JOptionPane.showMessageDialog(this, "Veillez renseigner tous les champs!");
        } else {
            Service ser = new Service(LibelleS, Integer.parseInt(NumS), Integer.parseInt(PrixUS));
            edao.inserer(ser);
            vider();
            liste_service();
        }
    }//GEN-LAST:event_btn_enregistrerActionPerformed

    private void btn_supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_supprimerActionPerformed
        int index = tb_service.getSelectedRow();

        if(index==-1) {
            JOptionPane.showMessageDialog(this, "Veillez selectionner une ligne!");
        } else {
            int id = (int) tb_service.getValueAt(index, 0);
            ServiceDAO edao = new ServiceDAO();
            edao.supprimer(id);
            vider();
            liste_service();
        }
    }//GEN-LAST:event_btn_supprimerActionPerformed

    private void btn_modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modifierActionPerformed
        ServiceDAO edao = new ServiceDAO();

        String LibelleS = txt_libelles.getSelectedItem().toString();
        String NumS = txt_nums.getText();
        String PrixUS = txt_prixus.getText();

        if (LibelleS.equals("") || NumS.equals("") || PrixUS.equals("")) {
            JOptionPane.showMessageDialog(this, "Veillez renseigner tous les champs!");
        } else {
            int index = tb_service.getSelectedRow();
            int id = (int) tb_service.getValueAt(index, 0);
            Service ser = new Service(LibelleS, Integer.parseInt(NumS), Integer.parseInt(PrixUS));
            edao.modifier(ser, id);
            vider();
            liste_service();
        }
    }//GEN-LAST:event_btn_modifierActionPerformed

    private void tb_serviceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_serviceMouseClicked
        int index = tb_service.getSelectedRow();

        if(index!=-1) {
            int id = (int) tb_service.getValueAt(index, 0);
            ServiceDAO edao = new ServiceDAO();
            Service serl = edao.rechercher(id);

            txt_libelles.setSelectedItem(serl.getLibelleS());
            txt_nums.setText(serl.getNumS()+"");
            txt_prixus.setText(serl.getPrixUS()+"");
        }
    }//GEN-LAST:event_tb_serviceMouseClicked


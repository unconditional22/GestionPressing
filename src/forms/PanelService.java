    private void liste_service() {
        ServiceDAO edao = new ServiceDAO();
        List<Service> service = edao.liste();
        
        for(int i = 0; i < service.size(); i++) {
            ((DefaultTableModel) tb_service.getModel()).addRow(new Object[] {
            service.get(i).getIDS(),
            service.get(i).getLibelleS(),
            service.get(i).getNumS(),
            service.get(i).getPrixUS(),
        });
       }
    }

   public void vider() {
        txt_libelles.setSelectedItem("");
        txt_nums.setText("");
        txt_prixus.setText("");
        
        DefaultTableModel model = (DefaultTableModel) tb_service.getModel();
        int n = model.getRowCount();
        for(int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
        }
    }
    
    public int calculService() {
     
        int rowsCount = tb_service.getRowCount();
        
        int total = 0; 
        for(int i = 0; i < rowsCount; i++){
            total = total+Integer.parseInt(tb_service.getValueAt(i, 3).toString());
        } 
        return total;
        //sum.setText(Integer.toString(calculer()));
     }
    

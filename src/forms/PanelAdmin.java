    private void txt_rechercherKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_rechercherKeyReleased
        DefaultTableModel model = (DefaultTableModel) tb_admin.getModel();
        TableRowSorter sorter = new TableRowSorter(model);
        tb_admin.setRowSorter(sorter);
        String rech = txt_rechercher.getText();
        sorter.setRowFilter(RowFilter.regexFilter(rech));
    }//GEN-LAST:event_txt_rechercherKeyReleased

    
    private void combo_caisse() {
        CaisseDAO edao = new CaisseDAO();
        List<Caisse> caisse = edao.liste();
        txt_caisse.addItem("");
        for(int i = 0; i < caisse.size(); i++) {
            txt_caisse.addItem(caisse.get(i).getID_Caisse()+"");
       }
    }
    
 
    private void liste_admin() {
        AdministrateurDAO edao = new AdministrateurDAO();
        List<Administrateur> administrateur = edao.liste();
        
        for(int i = 0; i < administrateur.size(); i++) {
            ((DefaultTableModel) tb_admin.getModel()).addRow(new Object[] {
            administrateur.get(i).getID_Admin(),
            administrateur.get(i).getNom(),
            administrateur.get(i).getPrenom(),
            administrateur.get(i).getLogin_admin(),
            administrateur.get(i).getPassword_admin(),
            administrateur.get(i).getProfil_admin(),
            administrateur.get(i).getID_Caisse()
            
        });
       }
    }

   public void vider() {
        txt_nom.setText("");
        txt_prenom.setText("");
        txt_login.setText("");
        txt_password.setText("");
        txt_profil.setSelectedItem("");
        txt_caisse.setSelectedItem("");
        
        DefaultTableModel model = (DefaultTableModel) tb_admin.getModel();
        int n = model.getRowCount();
        for(int i = n - 1; i >= 0; --i) {
            model.removeRow(i);
        }
    }
    

    private void tb_adminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_adminMouseClicked
        int index = tb_admin.getSelectedRow();
        
        if(index!=-1) {
            int id = (int) tb_admin.getValueAt(index, 0);
            AdministrateurDAO edao = new AdministrateurDAO();
            Administrateur adl = edao.rechercher(id);
            
            txt_nom.setText(adl.getNom());
            txt_prenom.setText(adl.getPrenom());
            txt_login.setText(adl.getLogin_admin());
            txt_password.setText(adl.getPassword_admin());
            txt_profil.setSelectedItem(adl.getProfil_admin());
            txt_caisse.setSelectedItem(adl.getID_Caisse()+"");
        }
    }//GEN-LAST:event_tb_adminMouseClicked

